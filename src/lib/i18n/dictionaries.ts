type Dictionary = typeof dictionaries.es;

export type TranslationKey = {
  [Section in keyof Dictionary]: {
    [Key in keyof Dictionary[Section]]: `${Section}.${Extract<Key, string>}`;
  }[keyof Dictionary[Section]];
}[keyof Dictionary];

export function translate(locale: Locale, key: TranslationKey): string {
  const [section, entry] = key.split('.') as [keyof Dictionary, string];
  const localizedSection = dictionaries[locale][section] as Record<string, string>;
  const fallbackSection = dictionaries.es[section] as Record<string, string>;

  return localizedSection[entry] ?? fallbackSection[entry] ?? key;
}
